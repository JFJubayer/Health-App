// standard food-composition data for the ingredient mix and portion
// size given. Treat them as good working estimates for an MVP, not
// lab-verified values — the usual caveat for any food-logging app.
//
// To add a dish: copy an existing FoodItem, give it a unique id,
// and make sure every ingredientId in `ingredients` exists in
// ingredient_prices.dart (otherwise cost calculation silently
// skips that ingredient).

